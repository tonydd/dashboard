    /**
     * @param string $term
     * @return array<Ingredient>
     */
    public function getAutocompleteResults(string $term): array
    {
        return $this->createQueryBuilder('i')
            ->andWhere('i.slug LIKE :term')
            ->setParameter('term', $term . '%')
            ->getQuery()
            ->getResult();
